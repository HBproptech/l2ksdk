class LKBuilding {
  String id;

  LKBuilding({required this.id});

  Map<String, dynamic> toJson() {
    return {
      'id': id,
    };
  }

  factory LKBuilding.fromJson(Map<String, dynamic> map) {
    return LKBuilding(id: map['id']);
  }
}
